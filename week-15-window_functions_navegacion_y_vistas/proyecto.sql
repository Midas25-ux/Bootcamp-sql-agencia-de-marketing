-- ============================================
-- CONSULTA 1: LAG para calcular la variación entre períodos
-- ============================================

SELECT
    period_date,
    channel_id,
    revenue,
    LAG(revenue) OVER (PARTITION BY channel_id ORDER BY period_date) AS prev_revenue,
    revenue - LAG(revenue) OVER (PARTITION BY channel_id ORDER BY period_date) AS delta
FROM channel_monthly_metrics
ORDER BY channel_id, period_date;

-- ============================================
-- CONSULTA 2: FIRST_VALUE y LAST_VALUE por canal
-- ============================================

SELECT
    period_date,
    channel_id,
    revenue,
    FIRST_VALUE(revenue) OVER w AS period_best,
    LAST_VALUE(revenue)  OVER w AS period_worst
FROM channel_monthly_metrics
WINDOW w AS (
    PARTITION BY channel_id
    ORDER BY revenue DESC
    ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING
)
ORDER BY channel_id, period_date;

-- ============================================
-- CONSULTA 3: CREATE VIEW — encapsular el análisis
-- ============================================

CREATE OR REPLACE VIEW v_period_analysis AS
SELECT
    period_date,
    channel_id,
    revenue,
    LAG(revenue) OVER (PARTITION BY channel_id ORDER BY period_date) AS prev_revenue,
    FIRST_VALUE(revenue) OVER (
        PARTITION BY channel_id
        ORDER BY revenue DESC
        ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING
    ) AS period_best,
    LAST_VALUE(revenue) OVER (
        PARTITION BY channel_id
        ORDER BY revenue DESC
        ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING
    ) AS period_worst
FROM channel_monthly_metrics;

SELECT *
FROM v_period_analysis
WHERE channel_id = 1
ORDER BY period_date;
